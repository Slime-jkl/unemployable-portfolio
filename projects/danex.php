    <section>
      <h2>âœ¨ Features</h2>
      <ul>
        <li>Invoice</li>
        <ul><li>Invoices can be uploaded into the system by attaching the pdf file, and inserting details of the
          invoice in a form.
        </li></ul>
        <ul><li>Invoices then can be organized, sorted and searched. Marking them as Paid/Unpaid/Overdue and keeping
          track of monthly income/expenses, feeding all the data into a graph.
        </li></ul>



        <li>Driver Management</li>
        <ul><li>The driver system allows admins to create new drivers and vehicles, upload relevant documents like
          Passports, driving license, Insurance Certificate, Goods In Transit Certificate and other field related docs.
          Keeping track of document expiry dates and showing warnings when a document is about to expire (in the next 30 days)
          and when documents have expired.
        </li></ul>
        <ul><li>Drivers can be tied to vehicles, which can then be tied to the invoices entered in the system, keeping track
          what drivers did what jobs and attaching driver notes for any on-site related issues.
        </li></ul>



        <li>Expenses</li>
        <ul><li>A system that keeps track of expenses, organizing them in different categories (Fuel, Tolls, Maintenance,
           ..etc) also allowing to attach pictures with receipts and notes. These can be introduced manually by the admin
           or driver accounts can be allowed to access the website and submit their own.
        </li></ul>

        
        <li>PDF Reports</li>
        <ul><li>A system that allows the user to pick a start and end date, generating a detailed list that reports income
          and expenses for that time period, along with document design to personalize the reports with the company logo
          and other relevant information. 
        </li></ul>
      </ul>

